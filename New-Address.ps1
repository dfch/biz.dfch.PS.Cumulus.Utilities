function New-Address {
<#
.SYNOPSIS
Creates a new IP address


.DESCRIPTION
Creates a new IP address

Creates a new IP address in Cumulus and in its connected backend IPAM and DNS 
systems. If specified VLAN name does not exist an error is thrown. If the an 
IP Address is specified as value and it already exists an error is thrown.


.LINK
Online Version: http://dfch.biz/biz/dfch/PS/Cumulus/Utilities/New-Address/


.EXAMPLE
$address = New-Address -Name Server42 -Description 'This is a new server entry' -VlanName 'VAR000'
$address

Id           : 31337
Name         : Server42
Value        : 192.0.2.15
Description  : This is a new server entry
Reference    : fixedaddress/496ca00323a7405781d25212cd17e6ee10.243.56.110/default
Type         : fixedaddress
Status       : Allocated
Address_Vlan : 000

This creates a new entry 'Server42' in the VLAN 'VAR000' and assigns the next available IP address to it


.NOTES
See module manifest for dependencies and further requirements.


#>
PARAM
(
	# Specifies the name of the new IP address
	[ValidatePattern('^([a-zA-Z0-9\.\-_]{1,255})|(\<empty\>)$')]
	[Parameter(Mandatory = $true, Position = 0)]
	[string] $Name
	,
	# Specfies the value of the new IP address. Currently only IPv4 supported
	[ValidateScript( { $_.AddressFamily -eq [System.Net.Sockets.AddressFamily]::InterNetwork} )]
	[Parameter(Mandatory = $false, Position = 1)]
	[ValidateNotNullOrEmpty()]
	[IPAddress] $Value = '0.0.0.0'
	,
	# Specifies the description of the new address
	[Parameter(Mandatory = $false)]
	[string] $Description
	,
	# Specifies the address type to create. Currently only 'fixedaddress' supported
	[ValidateSet('fixedaddress')]
	[Parameter(Mandatory = $false)]
	[string] $Type = 'fixedaddress'
	,
	# Specifies the status of the new address. Currently only 'Allocated' supported
	[ValidateSet('Allocated')]
	[Parameter(Mandatory = $false)]
	[string] $Status = 'Allocated'
	,
	# Specifies the name of Vlan from which the address should be allocated
	[ValidateNotNullOrEmpty()]
	[Parameter(Mandatory = $true)]
	[string] $VlanName
	,
	# Service reference to Cumulus
	[Parameter(Mandatory = $false)]
	[Alias("Services")]
	[hashtable] $svc = (Get-Variable -Name $MyInvocation.MyCommand.Module.PrivateData.MODULEVAR -ValueOnly).Services
	,
	# Specifies the return format of the Cmdlet
	[ValidateSet('default', 'json', 'json-pretty', 'xml', 'xml-pretty', 'entity', 'OperationResponse')]
	[Parameter(Mandatory = $false)]
	[Alias("ReturnFormat")]
	[string] $As = 'default'
)

BEGIN 
{

$datBegin = [datetime]::Now;
[string] $fn = $MyInvocation.MyCommand.Name;
Log-Debug -fn $fn -msg ("CALL. svc '{0}'. Name '{1}'." -f ($svc -is [Object]), $Name) -fac 1;

}
# BEGIN

PROCESS
{

# Default test variable for checking function response codes.
[Boolean] $fReturn = $false;
# Return values are always and only returned via OutputParameter.
$OutputParameter = $null;

try 
{

	# Parameter validation
	if($svc.ApplicationData -isnot [CumulusWrapper.ApplicationData.ApplicationData]) 
	{
		$msg = "svc: Parameter validation FAILED. Connect to the server before using the Cmdlet.";
		$e = New-CustomErrorRecord -m $msg -cat InvalidData -o $svc.ApplicationData;
		throw($gotoError);
	}
	$entity = $svc.ApplicationData.Addresses.AddQueryOption('$filter',("Value eq '{0}'" -f $Value.IPAddressToString)).AddQueryOption('$top',1) | Select;
	if($entity)
	{
		$msg = "Value: Parameter validation FAILED. '{0}' already exists. Cannot create new address." -f $Value.IPAddressToString;
		Log-Error $fn $msg;
		$e = New-CustomErrorRecord -m $msg -cat ResourceExists -o $Value;
		$PSCmdlet.ThrowTerminatingError($e);
		# throw($gotoError);
	}
	
	$Vlan = $svc.ApplicationData.Vlans.AddQueryOption('$filter',("Name eq '{0}'" -f $VlanName)).AddQueryOption('$top', 1) | Select;
	if(!$Vlan)
	{
		$msg = "VlanName: Parameter validation FAILED. '{0}' does not exist. Cannot create new address." -f $VlanName;
		Log-Error $fn $msg;
		$e = New-CustomErrorRecord -m $msg -cat ObjectNotFound -o $Value;
		$PSCmdlet.ThrowTerminatingError($e);
		# throw($gotoError);
	}

	$entity = New-Object CumulusWrapper.ApplicationData.Address;
	$svc.ApplicationData.AddToAddresses($entity);
	$entity.Name = $Name;
	$entity.Value = $Value.IPAddressToString;
	$entity.Type = $Type;
	$entity.Status = $Status;
	$entity.Reference = ' ';
	if($PSBoundParameters.ContainsKey('Description'))
	{
		$entity.Description = $Description;
	}
	$entity.Address_Vlan = $Vlan.Id;
	$svc.ApplicationData.SetLink($entity, 'Vlan', $Vlan);
	$svc.ApplicationData.UpdateObject($entity);
	$r = $svc.ApplicationData.SaveChanges();
	switch($As) 
	{
		'xml' { $OutputParameter = (ConvertTo-Xml -InputObject $r).OuterXml; }
		'xml-pretty' { $OutputParameter = Format-Xml -String (ConvertTo-Xml -InputObject $r).OuterXml; }
		'json' { $OutputParameter = ConvertTo-Json -InputObject $r -Compress; }
		'json-pretty' { $OutputParameter = ConvertTo-Json -InputObject $r; }
		'OperationResponse' { $OutputParameter = $r; }
		'entity' { $OutputParameter = $entity; }
		Default { $OutputParameter = $entity; }
	}
	$fReturn = $true;
}

catch 
{
	if($gotoSuccess -eq $_.Exception.Message) 
	{
		$fReturn = $true;
	} 
	else 
	{
		[string] $ErrorText = "catch [$($_.FullyQualifiedErrorId)]";
		$ErrorText += (($_ | fl * -Force) | Out-String);
		$ErrorText += (($_.Exception | fl * -Force) | Out-String);
		$ErrorText += (Get-PSCallStack | Out-String);
		
		if($_.Exception -is [System.Net.WebException]) 
		{
			Log-Critical $fn ("[WebException] Request FAILED with Status '{0}'. [{1}]." -f $_.Status, $_);
			Log-Debug $fn $ErrorText -fac 3;
		}
		else 
		{
			Log-Error $fn $ErrorText -fac 3;
			if($gotoError -eq $_.Exception.Message) 
			{
				Log-Error $fn $e.Exception.Message;
				$PSCmdlet.ThrowTerminatingError($e);
			} 
			elseif($gotoFailure -ne $_.Exception.Message) 
			{ 
				Write-Verbose ("$fn`n$ErrorText"); 
			} 
			else 
			{
				# N/A
			}
		}
		$fReturn = $false;
		$OutputParameter = $null;
	}
}
finally 
{
	# Clean up
	# N/A
}

}
# PROCESS

END 
{
	$datEnd = [datetime]::Now;
	Log-Debug -fn $fn -msg ("RET. fReturn: [{0}]. Execution time: [{1}]ms. Started: [{2}]." -f $fReturn, ($datEnd - $datBegin).TotalMilliseconds, $datBegin.ToString('yyyy-MM-dd HH:mm:ss.fffzzz')) -fac 2;
	# Return values are always and only returned via OutputParameter.
	return $OutputParameter;
}
# END

} # function
if($MyInvocation.ScriptName) { Export-ModuleMember -Function New-Address; } 

